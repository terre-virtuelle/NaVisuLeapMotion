package bzh.terrevirtuelle.navisuleapmotion.util;

import java.net.URI;
import java.net.URISyntaxException;
import java.util.logging.Level;
import java.util.logging.Logger;

/**
 *
 * @author Di Falco Nicola
 */
public class WebsocketClient {
    public static void main(String[] args) {
        URI uri = URI.create("ws://localhost:8787");
        System.out.println("Connecting to "+uri);
        final WebsocketClientEndpoint clientEndPoint = new WebsocketClientEndpoint(uri);
        clientEndPoint.addMessageHandler((String message) -> {
            System.out.println(message);
        });
    }
}
